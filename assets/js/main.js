// Function to create a skill card
function createSkillCard(skill) {
  const skillCard = document.createElement("div");
  skillCard.className = "col-md-4 col-sm-6 skill-card"; // Adjust column classes for desired layout

  const skillIcon = document.createElement("i");
  skillIcon.className = `skill-icon ${skill.icon}`;

  const skillTitle = document.createElement("div");
  skillTitle.className = "skill-title";
  skillTitle.textContent = skill.name;

  skillCard.appendChild(skillIcon);
  skillCard.appendChild(skillTitle);

  return skillCard;
}

// Function to display skills one at a time with a zoom-in effect
function displaySkills() {
  const skillsContainer = document.getElementById("skills-container");

  softSkills.forEach(skill => {
    const skillCard = createSkillCard(skill);
    skillsContainer.appendChild(skillCard);

    // Apply a zoom-in effect with a CSS class
    skillCard.classList.add("zoom-in");
  });
}

